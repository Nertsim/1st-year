    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = textField1.getText();
        String password = textField2.getText();
        try {
            User user = AtmWithGuiFinal.getDbManager().getUser(username, AtmWithGuiFinal.getTransactionManager());
            if (user != null && user.getPassword().equals(password)) {
                AtmWithGuiFinal.setCurrentUser(user);
                AtmWithGuiFinal.getLoggedInPanel().jTextField1.setText(username);
                AtmWithGuiFinal.showPanel(AtmWithGuiFinal.getLoggedInPanel().getPanel());
            } else {
                JOptionPane.showMessageDialog(this, "Invalid username or password", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


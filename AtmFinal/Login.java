    private void login() {
        String fullName = textField1.getText().trim();
        String pin = new String(jPasswordField1.getPassword()).trim();

        if (fullName.isEmpty() || pin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!");
            return;
        }

        try (Connection conn = AtmFinal.getConnection(); PreparedStatement stmt = conn.prepareStatement(
                "SELECT pin FROM users WHERE full_name = ?")) {
            stmt.setString(1, fullName);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                String storedPin = rs.getString("pin");
                if (pin.equals(storedPin)) {
                    AtmFinal.setLoggedInUser(fullName);
                    AtmFinal.showPanel(new Menu());
                } else {
                    JOptionPane.showMessageDialog(this, "Incorrect pin!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "User not found!");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

